<h3 data-magellan-destination="vision" class="blog-header-large">Vision</h3>
<p class="blog-p"><strong>Imagine a world where almost all the products we use are manufactured locally using sustainable practices</strong>. We work with people throughout the world to achieve this.</p>

<p class="blog-p">Rather than thinking of ideas as private property, we view innovation as a public resource that can be made available to the commons for everyone’s benefit. This inspires the growth of global networks of collaboration, where entrepreneurs in Spain or Guatemala or China work together to co-develop products that they can each sell in their local markets under their own unique branding (no-strings franchising).</p>

<p class="blog-p">Entrepreneurship is our greatest weapon against poverty and the destruction of the environment. Allowing others to manufacture products we design helps them create businesses where they live, anywhere in the world, and vice versa. In return, we have potential partners in innovation in every corner of the planet. Forgoing the right to patent is like an open invitation to everyone and anyone to help us innovate solutions to humanity’s most pressing needs, and we all benefit.</p>
</div>


<div class="large-11 columns large-centered">
<a name="open-source-hardware"></a>
<h3 data-magellan-destination="open-source-hardware" class="blog-header-large">Open Source Hardware</h3>
<p class="blog-p"><a href="http://www.oshwa.org/definition/">Open Source Hardware (OSHW) Statement of Principles 1.0:</a>
  <blockquote>"Open source hardware is hardware whose design is made publicly available so that anyone can study, modify, distribute, make, and sell the design or hardware based on that design. The hardware’s source, the design from which it is made, is available in the preferred format for making modifications to it. Ideally, open source hardware uses readily-available components and materials, standard processes, open infrastructure, unrestricted content, and open-source design tools to maximize the ability of individuals to make and use hardware. Open source hardware gives people the freedom to control their technology while sharing knowledge and encouraging commerce through the open exchange of designs."<cite>The Open Source Hardware Association</cite></blockquote>
</p>
</div>


<div class="large-11 columns large-centered">
  <a name="do"></a>
  <h3 data-magellan-destination="do" class="blog-header-large">What We Do</h3>
  
  <p class="blog-p">We develop <strong>open source commercial products</strong> and provide <strong>public education programs</strong>. All of our work is designed to have a social benefit. For each product we design, we publish its source documentation <em>(3D models, fabrication diagrams, bills of materials, instructional videos, etc.)</em> under a <a href="http://creativecommons.org/licenses/by-sa/3.0/">100% free and open source license</a>. While we are still in our first year of development, we have multiple open hardware projects that are nearly ready for beta testing as of January 1, 2014, and we have hosted a number of workshops and community events that are aimed at economic development and environmental restoration.</p>

  <p class="blog-p">In the future, we will most likely establish a nonprofit and for-profit hybrid organization. Both will have a charitable purpose and will be organized to provide a social benefit, but the nonprofit will allow for individuals and foundations to grant tax-deductible contributions to our work, while the for-profit branch will fund our operations through the commercialization of products and services.</p>
</div>
